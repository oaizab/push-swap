#include "push_swap.h"

int	main()
{
	t_stack *a = ft_stack();
	t_stack *b = ft_stack();
	int t[] = {10, 7, 3, 8, 9, 0, 4, 6, 1, 2};
	for (int i = 9; i >= 0; i--)
	{
		ft_push(a, t[i]);
	}
	push_b(a, b);
	ft_show(*a, *b);
	return 0;
}
